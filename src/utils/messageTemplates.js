function getTemplate(type, subtype, babyName, age) {
    if (type === 'greeting') {
        return messageTemplates.generateGreeting(babyName, age);
    }
    if (type === 'feeding') {
        const feedingTemplates = messageTemplates.generateFeeding(babyName, age);
        return subtype ? feedingTemplates[subtype] : feedingTemplates;
    }
    if (type === 'activities') {
        const activityTemplates = messageTemplates.generateActivities(babyName, age);
        return subtype ? activityTemplates[subtype] : activityTemplates;
    }
    if (type === 'specialEvents') {
        const specialEventsTemplates = messageTemplates.specialEvents;
        return subtype ? specialEventsTemplates[subtype] : specialEventsTemplates;
    }
    return null;
}

